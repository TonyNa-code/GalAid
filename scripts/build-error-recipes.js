const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.join(__dirname, "..");
const DATA_PATH = path.join(ROOT, "data", "error-recipes.json");
const OUTPUT_PATH = path.join(ROOT, "src", "error-recipes.js");
const LEVELS = new Set(["info", "warning"]);

function readRecipes() {
  const raw = fs.readFileSync(DATA_PATH, "utf8");
  const recipes = JSON.parse(raw);
  validateRecipes(recipes);
  return recipes;
}

function validateRecipes(recipes) {
  const errors = [];
  const ids = new Set();

  if (!Array.isArray(recipes)) {
    throw new Error("data/error-recipes.json must contain a JSON array.");
  }

  recipes.forEach((recipe, index) => {
    const label = recipe && recipe.id ? recipe.id : `recipe at index ${index}`;

    for (const field of ["id", "title", "category", "level", "cause", "action"]) {
      if (!recipe || typeof recipe[field] !== "string" || !recipe[field].trim()) {
        errors.push(`${label}: missing string field "${field}"`);
      }
    }

    if (recipe?.id && !/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(recipe.id)) {
      errors.push(`${label}: id must be kebab-case`);
    }

    if (recipe?.level && !LEVELS.has(recipe.level)) {
      errors.push(`${label}: level must be one of ${[...LEVELS].join(", ")}`);
    }

    if (recipe?.id && ids.has(recipe.id)) {
      errors.push(`${label}: duplicate recipe id`);
    }
    if (recipe?.id) ids.add(recipe.id);

    if (!Array.isArray(recipe?.patterns) || recipe.patterns.length === 0) {
      errors.push(`${label}: patterns must be a non-empty array`);
    } else {
      recipe.patterns.forEach((pattern, patternIndex) => {
        if (typeof pattern !== "string" || !pattern.trim()) {
          errors.push(`${label}: pattern ${patternIndex + 1} must be a non-empty string`);
          return;
        }
        try {
          new RegExp(pattern, "i");
        } catch (error) {
          errors.push(`${label}: pattern ${patternIndex + 1} is not a valid RegExp: ${error.message}`);
        }
      });
    }

    if (!Array.isArray(recipe?.checklist) || recipe.checklist.some((step) => typeof step !== "string" || !step.trim())) {
      errors.push(`${label}: checklist must be an array of non-empty strings`);
    }
  });

  if (errors.length) {
    throw new Error(`Invalid error recipes:\n- ${errors.join("\n- ")}`);
  }
}

function buildJavaScript(recipes) {
  return [
    "// Generated from data/error-recipes.json. Do not edit this file directly.",
    "// Run `npm run build:recipes` after changing the community recipe data.",
    `window.GALAID_ERROR_RECIPES = ${JSON.stringify(recipes, null, 2)};`,
    "",
  ].join("\n");
}

function main() {
  const recipes = readRecipes();
  const output = buildJavaScript(recipes);

  if (process.argv.includes("--check")) {
    const current = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, "utf8") : "";
    if (current !== output) {
      console.error("src/error-recipes.js is out of date. Run `npm run build:recipes`.");
      process.exit(1);
    }
    console.log(`Validated ${recipes.length} error recipes.`);
    return;
  }

  fs.writeFileSync(OUTPUT_PATH, output);
  console.log(`Wrote ${path.relative(ROOT, OUTPUT_PATH)} from ${path.relative(ROOT, DATA_PATH)}.`);
}

main();
