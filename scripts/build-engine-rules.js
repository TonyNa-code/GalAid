const fs = require("node:fs");
const path = require("node:path");

const ROOT = path.join(__dirname, "..");
const DATA_PATH = path.join(ROOT, "data", "engine-rules.json");
const OUTPUT_PATH = path.join(ROOT, "src", "engine-rules.js");
const MATCH_ARRAY_FIELDS = [
  "extensions",
  "filenames",
  "pathIncludes",
  "pathEndsWith",
  "filenameRegex",
  "pathRegex",
  "dllNameIncludes",
];
const REGEX_FIELDS = new Set(["filenameRegex", "pathRegex"]);

function readRules() {
  const raw = fs.readFileSync(DATA_PATH, "utf8");
  const rules = JSON.parse(raw);
  validateRules(rules);
  return rules;
}

function validateRules(rules) {
  const errors = [];
  const ids = new Set();

  if (!Array.isArray(rules)) {
    throw new Error("data/engine-rules.json must contain a JSON array.");
  }

  rules.forEach((rule, index) => {
    const label = rule && rule.id ? rule.id : `rule at index ${index}`;

    for (const field of ["id", "name", "description", "advice"]) {
      if (!rule || typeof rule[field] !== "string" || !rule[field].trim()) {
        errors.push(`${label}: missing string field "${field}"`);
      }
    }

    if (rule?.id && !/^[a-z0-9]+(?:-[a-z0-9]+)*$/.test(rule.id)) {
      errors.push(`${label}: id must be kebab-case`);
    }

    if (rule?.id && ids.has(rule.id)) {
      errors.push(`${label}: duplicate rule id`);
    }
    if (rule?.id) ids.add(rule.id);

    if (typeof rule?.score !== "number" || !Number.isFinite(rule.score) || rule.score < 0) {
      errors.push(`${label}: score must be a non-negative number`);
    }

    if (!rule?.match || typeof rule.match !== "object" || Array.isArray(rule.match)) {
      errors.push(`${label}: match must be an object`);
      return;
    }

    let hasMatcher = false;
    for (const field of MATCH_ARRAY_FIELDS) {
      const values = rule.match[field];
      if (values === undefined) continue;
      hasMatcher = true;

      if (!Array.isArray(values) || values.length === 0) {
        errors.push(`${label}: match.${field} must be a non-empty array`);
        continue;
      }

      values.forEach((value, valueIndex) => {
        if (typeof value !== "string" || !value.trim()) {
          errors.push(`${label}: match.${field}[${valueIndex}] must be a non-empty string`);
          return;
        }
        if (REGEX_FIELDS.has(field)) {
          try {
            new RegExp(value, "i");
          } catch (error) {
            errors.push(`${label}: match.${field}[${valueIndex}] is not a valid RegExp: ${error.message}`);
          }
        }
      });
    }

    for (const field of Object.keys(rule.match)) {
      if (!MATCH_ARRAY_FIELDS.includes(field)) errors.push(`${label}: unsupported matcher field "${field}"`);
    }

    if (!hasMatcher) errors.push(`${label}: match must include at least one matcher array`);
  });

  for (const requiredId of ["kirikiri", "commercial-proprietary"]) {
    if (!ids.has(requiredId)) errors.push(`missing required engine rule: ${requiredId}`);
  }

  if (errors.length) {
    throw new Error(`Invalid engine rules:\n- ${errors.join("\n- ")}`);
  }
}

function buildJavaScript(rules) {
  return [
    "// Generated from data/engine-rules.json. Do not edit this file directly.",
    "// Run `npm run build:engines` after changing the community engine rule data.",
    `window.GALAID_ENGINE_RULES = ${JSON.stringify(rules, null, 2)};`,
    "",
  ].join("\n");
}

function main() {
  const rules = readRules();
  const output = buildJavaScript(rules);

  if (process.argv.includes("--check")) {
    const current = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, "utf8") : "";
    if (current !== output) {
      console.error("src/engine-rules.js is out of date. Run `npm run build:engines`.");
      process.exit(1);
    }
    console.log(`Validated ${rules.length} engine rules.`);
    return;
  }

  fs.writeFileSync(OUTPUT_PATH, output);
  console.log(`Wrote ${path.relative(ROOT, OUTPUT_PATH)} from ${path.relative(ROOT, DATA_PATH)}.`);
}

main();
